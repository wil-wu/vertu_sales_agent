# 知识图谱问答对生成提示词
GRAPH_QA_GENERATION_PROMPT = """你是一个专业的知识图谱问答对生成专家。我会给你一段手机产品知识图谱数据，请根据数据内容生成高质量的中文问答对。

## 数据结构说明

输入数据包含以下字段：
- `p`：手机型号主节点，包含品牌、型号、系统、屏幕、电池等核心参数
- `type(r1)`：p 与 n1 之间的关系类型（如 HAS_VARIANT 表示"拥有变体/款式"）
- `n1`：变体节点，包含该款式的 SKU、颜色、存储、内存、地区等信息
- `type(r2)`：n1 与 n2 之间的关系类型（如 HAS_MATERIAL 表示"使用材质"）
- `collect(DISTINCT n2)`：n2 节点列表，通常为材质、配件、特性等扩展属性

## 生成规则

1. **覆盖维度要求**
   - 必须覆盖 p 节点的每一个非空属性，至少生成一个问答对
   - 必须覆盖 n1 节点的核心属性（SKU、颜色、存储规格）
   - 必须覆盖 n2 列表（材质组合、名称等）
   - 必须生成至少一个跨节点组合问题（将 p + n1 + n2 信息联合作答）

2. **问题类型要求**，每份数据至少包含以下五类：
   - 【直接查询】直接问某个属性值，如"XX手机的屏幕尺寸是多少？"
   - 【是否判断】用于验证事实，如"VERTU AGENT Q 是否支持 Android 15？"
   - 【枚举列举】列出所有可选项，如"XX款式有哪些可选材质？"
   - 【对比描述】描述某款式与标准型号的差异化特点
   - 【综合介绍】综合多个字段给出完整介绍性回答

3. **答案质量要求**
   - 答案必须完全基于给定数据，不得编造或推断
   - 答案语言自然流畅，像真人客服回答一样
   - 涉及列表的答案用顿号或逗号分隔，不使用 Markdown 列表符号
   - 答案中需自然带入产品名称，不能只说"该产品"

4. **输出格式要求**
   请严格按照以下 JSON 格式输出，不要添加任何其他说明文字：
   [
     {
       "question": "问题文本",
       "answer": "答案文本"
     }
   ]

图谱数据：
{graph_data}
"""

